// === Helper Functions ===

const HOME_EQUIPMENT: Equipment[] = [
  'none',
  'dumbbells',
  'resistance_band',
  'mat',
  'foam_roller',
  'pull_up_bar',
]

const OUTDOOR_EQUIPMENT: Equipment[] = ['none', 'resistance_band']

/**
 * Find an exercise by its unique ID.
 */
export function getExerciseById(id: string): Exercise | undefined {
  return EXERCISES.find((e) => e.id === id)
}

/**
 * Get all exercises belonging to a specific category.
 */
export function getExercisesByCategory(
  category: ExerciseCategory
): Exercise[] {
  return EXERCISES.filter((e) => e.category === category)
}

/**
 * Get all exercises that target a specific muscle group (primary or secondary).
 */
export function getExercisesByMuscle(muscle: MuscleGroup): Exercise[] {
  return EXERCISES.filter(
    (e) => e.primaryMuscle === muscle || e.muscleGroups.includes(muscle)
  )
}

/**
 * Get all exercises that require a specific piece of equipment.
 */
export function getExercisesByEquipment(equipment: Equipment): Exercise[] {
  return EXERCISES.filter((e) => e.equipment.includes(equipment))
}

/**
 * Get all exercises matching a specific difficulty level.
 */
export function getExercisesByDifficulty(level: FitnessLevel): Exercise[] {
  return EXERCISES.filter((e) => e.difficulty === level)
}

/**
 * Search exercises by name (German or English), description, or tags.
 * Case-insensitive matching.
 */
export function searchExercises(query: string): Exercise[] {
  const lowerQuery = query.toLowerCase()
  return EXERCISES.filter(
    (e) =>
      e.name.toLowerCase().includes(lowerQuery) ||
      e.nameEn.toLowerCase().includes(lowerQuery) ||
      e.description.toLowerCase().includes(lowerQuery) ||
      e.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
  )
}

/**
 * Get exercises suitable for a specific training location.
 * - 'home': exercises that only require home-friendly equipment
 * - 'outdoor': exercises that only require bodyweight or resistance bands
 * - 'gym': all exercises
 */
export function getExercisesForLocation(
  location: 'gym' | 'home' | 'outdoor'
): Exercise[] {
  if (location === 'gym') {
    return [...EXERCISES]
  }

  const allowedEquipment =
    location === 'home' ? HOME_EQUIPMENT : OUTDOOR_EQUIPMENT

  return EXERCISES.filter((e) =>
    e.equipment.every((eq) => allowedEquipment.includes(eq))
  )
}
