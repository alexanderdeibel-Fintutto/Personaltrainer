// Helper Functions
// =============================================================================

/**
 * Search foods by name, brand, or category.
 * Case-insensitive partial matching.
 */
export function searchFoods(query: string): FoodItem[] {
  const lowerQuery = query.toLowerCase().trim()
  if (!lowerQuery) return []

  return FOODS.filter(
    (food) =>
      food.name.toLowerCase().includes(lowerQuery) ||
      food.category.toLowerCase().includes(lowerQuery) ||
      (food.brand && food.brand.toLowerCase().includes(lowerQuery))
  )
}

/**
 * Get all foods in a specific category.
 */
export function getFoodsByCategory(category: string): FoodItem[] {
  return FOODS.filter(
    (food) => food.category.toLowerCase() === category.toLowerCase()
  )
}

/**
 * Get a food item by its unique ID.
 */
export function getFoodById(id: string): FoodItem | undefined {
  return FOODS.find((food) => food.id === id)
}

/**
 * Calculate nutritional values for a given amount (in grams) of a food item.
 * Returns null if the food is not found.
 */
export function calculateNutrition(
  foodId: string,
  grams: number
): {
  calories: number
  protein: number
  carbs: number
  fat: number
  fiber: number
} | null {
  const food = getFoodById(foodId)
  if (!food) return null

  const factor = grams / 100

  return {
    calories: Math.round(food.caloriesPer100g * factor),
    protein: Math.round(food.proteinPer100g * factor * 10) / 10,
    carbs: Math.round(food.carbsPer100g * factor * 10) / 10,
    fat: Math.round(food.fatPer100g * factor * 10) / 10,
    fiber: Math.round(food.fiberPer100g * factor * 10) / 10,
  }
}
