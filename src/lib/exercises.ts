// Merge base + extended exercises (deduplicate by ID)
const seenIds = new Set<string>()
export const EXERCISES: Exercise[] = [...EXERCISES_BASE, ...EXERCISES_EXTENDED].filter(e => {
  if (seenIds.has(e.id)) return false
  seenIds.add(e.id)
  return true
})

// --- Helper Functions ---

export function getExerciseById(id: string): Exercise | undefined {
  return EXERCISES.find(e => e.id === id)
}

export function getExercisesByMuscle(muscle: string): Exercise[] {
  return EXERCISES.filter(e =>
    e.primaryMuscles.includes(muscle as any) || e.secondaryMuscles.includes(muscle as any)
  )
}

export function getExercisesByEquipment(eq: string): Exercise[] {
  return EXERCISES.filter(e => e.equipment.includes(eq as any))
}

export function getExercisesByLocation(loc: string): Exercise[] {
  return EXERCISES.filter(e => e.locations.includes(loc as any))
}

export function getExercisesByCategory(cat: string): Exercise[] {
  return EXERCISES.filter(e => e.category === cat)
}

export function searchExercises(query: string): Exercise[] {
  const q = query.toLowerCase()
  return EXERCISES.filter(e =>
    e.name.toLowerCase().includes(q) ||
    (e.nameEn && e.nameEn.toLowerCase().includes(q))
  )
}
